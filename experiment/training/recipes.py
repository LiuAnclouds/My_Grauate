def _known_label_dim(dataset_name: str) -> str:
    spec = get_dataset_spec(dataset_name)
    return "5" if len(spec.background_labels) >= 2 else "3"


def _prototype_num_classes(dataset_name: str) -> str:
    spec = get_dataset_spec(dataset_name)
    if len(spec.background_labels) >= 2:
        return "4"
    if len(spec.background_labels) == 1:
        return "3"
    return "2"


def _semantic_aligned_known_label_dim(dataset_name: str) -> str:
    _ = dataset_name
    return "3"


def _semantic_aligned_prototype_num_classes(dataset_name: str) -> str:
    _ = dataset_name
    return "2"


